package one;

public class dayOne {

	public static void main(String[] args) {
		
	
		System.out.println("Hello");
		System.out.println("Bye");

		
		// Arithmetic operator
		
		// Addition       --> + 
		// Subtraction    --> -
		// Multiplication --> *
		// Division       --> /
		// Modulus        --> %
		
		
		int x = 10;
		int y = 5;
		
		System.out.println("x");
		System.out.println(x);
		System.out.println(y);
		
		
		
		System.out.println(x+y); 
		System.out.println(x-y); 
		System.out.println(x*y); 
		System.out.println(x/y); 
		System.out.println(x%y); 
		System.out.println(23%5); 
		
		
		int a = 8;
		int b = 4;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		


	}

}
